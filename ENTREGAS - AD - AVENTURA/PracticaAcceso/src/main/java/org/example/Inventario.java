package org.example;

public class Inventario {

    public static boolean espada = false;

    public static boolean caliz = false;

    public static boolean orbe = false;

    public Inventario() {
    }

    public boolean isEspada() {
        return espada;
    }

    public void setEspada(boolean espada) {
        this.espada = espada;
    }

    public boolean isCaliz() {
        return caliz;
    }

    public void setCaliz(boolean caliz) {
        this.caliz = caliz;
    }

    public boolean isOrbe() {
        return orbe;
    }

    public void setOrbe(boolean orbe) {
        this.orbe = orbe;
    }
}
